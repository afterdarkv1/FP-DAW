# ======================================================================
# FUNCIONES DE ENTRADA Y SALIDA
# ======================================================================

# input()      → Permite pedir datos al usuario por teclado.
# print()      → Muestra información por pantalla.


# ======================================================================
# FUNCIONES NUMÉRICAS
# ======================================================================

# int()        → Convierte una cadena o dato a número entero.
# float()      → Convierte a número decimal.
# max()        → Devuelve el valor máximo de una lista o conjunto.
# min()        → Devuelve el valor mínimo de una lista o conjunto.
# sum()        → Suma todos los valores de una lista.
# round()      → Redondea números (ej: round(num, 2) → 2 decimales).


# ======================================================================
# FUNCIONES PARA LISTAS Y ARRAYS
# ======================================================================

# append()     → Añade un elemento al final de una lista.
# split()      → Separa un string en palabras y devuelve una lista.
# sorted()     → Ordena una lista y devuelve una nueva lista ordenada.
# enumerate()  → Permite recorrer listas obteniendo índice y valor.
# len()        → Devuelve la cantidad de elementos de una lista.
# list comprehension [x for x in lista] → Forma compacta de recorrer/filtrar listas.


# ======================================================================
# FUNCIONES PARA CADENAS DE TEXTO (STRINGS)
# ======================================================================

# lower()      → Convierte todas las letras a minúsculas.
# upper()      → Convierte todas las letras a mayúsculas.
# split()      → (Repetida arriba) Divide un texto por espacios.
# isdigit()    → Devuelve True si el carácter es un número del 0 al 9.
# replace()    → Reemplaza partes de un texto por otro (aunque no fue usada directamente).


# ======================================================================
# FUNCIONES PARA CONJUNTOS (SET)
# ======================================================================

# set()                    → Crea un conjunto (colección sin duplicados).
# intersection(otro_set)   → Devuelve las coincidencias entre dos conjuntos.


# ======================================================================
# FUNCIONES DEL MÓDULO RANDOM
# ======================================================================

# random.sample(lista, n)  → Devuelve 'n' elementos aleatorios sin repetir.
# random.randint(a, b)     → Genera un número aleatorio entre 'a' y 'b' incluido.


# ======================================================================
# FUNCIONES DEL MÓDULO MATH
# ======================================================================

# math.sqrt()              → Devuelve la raíz cuadrada de un número.


# ======================================================================
# FUNCIONES DEL MÓDULO COLLECTIONS (Counter)
# ======================================================================

# Counter(lista)           → Cuenta cuántas veces aparece cada elemento.
# most_common(1)           → Devuelve el elemento más repetido y su cantidad.


# ======================================================================
# ESTRUCTURAS DE CONTROL (no son funciones, pero sí herramientas usadas)
# ======================================================================

# for                      → Bucle que recorre listas, rangos o iterables.
# while                    → Bucle que se ejecuta mientras una condición sea verdadera.
# if / elif / else         → Condiciones para ejecutar diferentes bloques de código.
# match-case               → Alternativa al switch-case para seleccionar acciones.


# ======================================================================
# FUNCIONES PARA DICCIONARIOS
# ======================================================================

# {}                       → Crea un diccionario vacío.
# get(clave, valor_def)    → Obtiene el valor de una clave sin error si no existe.
# items()                  → Devuelve pares (clave, valor).
# values()                 → Devuelve solo los valores.
# keys()                   → Devuelve solo las claves.